const namedColors = {
  black: '#000000',
  white: '#ffffff',
};

export const resolveTapeColor = (className, fallback = '#111827') => {
  if (!className) return fallback;
  const colorName = className.replace(/^(bg|border)-/, '');
  if (namedColors[colorName]) return namedColors[colorName];

  const match = colorName.match(/^(.+)-(\d+)$/);
  if (!match) return fallback;

  const [, palette, shade] = match;
  return palettes[palette]?.[shade] || fallback;
};
